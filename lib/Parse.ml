(*
   Costly operation that translates a whole tree or subtree.

   The first pass translates it into a generic tree structure suitable
   to guess which node corresponds to each grammar rule.
   The second pass is a translation into a typed tree where each grammar
   node has its own type.

   This function is called:
   - once on the root of the program after removing extras
     (comments and other nodes that occur anywhere independently from
     the grammar);
   - once of each extra node, resulting in its own independent tree of type
     'extra'.
*)
let translate_tree src node trans_x =
  let matched_tree = Run.match_tree children_regexps src node in
  Option.map trans_x matched_tree


let translate_extra src (node : Tree_sitter_output_t.node) : CST.extra option =
  match node.type_ with
  | "comment" ->
      (match translate_tree src node trans_comment with
      | None -> None
      | Some x -> Some (`Comment (Run.get_loc node, x)))
  | "preproc_region" ->
      (match translate_tree src node trans_preproc_region with
      | None -> None
      | Some x -> Some (`Preproc_region (Run.get_loc node, x)))
  | "preproc_endregion" ->
      (match translate_tree src node trans_preproc_endregion with
      | None -> None
      | Some x -> Some (`Preproc_endregion (Run.get_loc node, x)))
  | "preproc_line" ->
      (match translate_tree src node trans_preproc_line with
      | None -> None
      | Some x -> Some (`Preproc_line (Run.get_loc node, x)))
  | "preproc_pragma" ->
      (match translate_tree src node trans_preproc_pragma with
      | None -> None
      | Some x -> Some (`Preproc_pragma (Run.get_loc node, x)))
  | "preproc_nullable" ->
      (match translate_tree src node trans_preproc_nullable with
      | None -> None
      | Some x -> Some (`Preproc_nullable (Run.get_loc node, x)))
  | "preproc_error" ->
      (match translate_tree src node trans_preproc_error with
      | None -> None
      | Some x -> Some (`Preproc_error (Run.get_loc node, x)))
  | "preproc_warning" ->
      (match translate_tree src node trans_preproc_warning with
      | None -> None
      | Some x -> Some (`Preproc_warning (Run.get_loc node, x)))
  | "preproc_define" ->
      (match translate_tree src node trans_preproc_define with
      | None -> None
      | Some x -> Some (`Preproc_define (Run.get_loc node, x)))
  | "preproc_undef" ->
      (match translate_tree src node trans_preproc_undef with
      | None -> None
      | Some x -> Some (`Preproc_undef (Run.get_loc node, x)))
  | _ -> None

let translate_root src root_node =
  translate_tree src root_node trans_compilation_unit

let parse_input_tree input_tree =
  let orig_root_node = Tree_sitter_parsing.root input_tree in
  let src = Tree_sitter_parsing.src input_tree in
  let errors = Run.extract_errors src orig_root_node in
  let opt_program, extras =
     Run.translate
       ~extras
       ~translate_root:(translate_root src)
       ~translate_extra:(translate_extra src)
       orig_root_node
  in
  Parsing_result.create src opt_program extras errors

let string ?src_file contents =
  let input_tree = parse_source_string ?src_file contents in
  parse_input_tree input_tree

let file src_file =
  let input_tree = parse_source_file src_file in
  parse_input_tree input_tree

